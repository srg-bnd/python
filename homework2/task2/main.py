# Задание 2. Подсчёт стоимости заказа из файла
# Напишите программу, которая считывает файл prices.txt, содержащий информацию о товарах:
#   название, количество и цену, и подсчитывает общую стоимость заказа.

# Инициализируем переменную для общей суммы
total_cost = 0.0

# Открываем файл и читаем его построчно
with open('../data/prices.txt', 'r', encoding='utf-8') as file:
    for line in file:
        # Разбиваем строку по пробелам/табуляциям
        parts = line.strip().split()
        
        # Проверяем, что в строке достаточно данных
        if len(parts) >= 3:
            try:
                # Преобразуем количество и цену в числа
                quantity = float(parts[1])
                price = float(parts[2])
                # Считаем стоимость позиции и добавляем к общей сумме
                total_cost += quantity * price
            except ValueError:
                print(f"Ошибка в данных строки: {line}")

# Выводим результат с двумя знаками после запятой
print(f"Общая стоимость заказа: {total_cost:.2f} руб.")
